const SUPPORTED_LANGS = ['en', 'fr', 'ja', 'es', 'de'];
const LANG_LABELS = { en: '🇬🇧 EN', fr: '🇫🇷 FR', ja: '🇯🇵 JA', es: '🇪🇸 ES', de: '🇩🇪 DE' };

function setLang(code) {
  const lang = SUPPORTED_LANGS.includes(code) ? code : 'en';
  const t = TRANSLATIONS[lang];
  document.documentElement.lang = lang === 'ja' ? 'ja' : lang;
  localStorage.setItem('fudami-lang', lang);

  // Plain text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // innerHTML nodes (contain styled spans)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Update lang selector to show current
  const sel = document.getElementById('lang-select');
  if (sel) sel.value = lang;
}

function initLang() {
  const saved = localStorage.getItem('fudami-lang');
  const browser = (navigator.language || 'en').slice(0, 2).toLowerCase();
  const lang = saved || (SUPPORTED_LANGS.includes(browser) ? browser : 'en');
  setLang(lang);
}
